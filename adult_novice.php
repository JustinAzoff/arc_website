<p>
The Adult Novice Program, new for 2010, is intended to help rowers transition
from the <a href="/adult_learn_to_row.php">Adult Learn to Row Program</a> into
ARC's programs for experienced rowers.  Novice rowers will get extra attention
from the coach, with emphasis on technique, while starting to put in some miles
on the water.
</p>

<p>
Participants may transfer into the Novice program after one or more sessions of
<a href="/adult_learn_to_row.php">Learn to Row</a>, or may start out in the
Novice program if you have rowed previously but would like a chance to brush up
on your technique before joining ARC's programs for experienced rowers.
Participants may transfer into the <a href="/adult_recreational.php">Adult
Evening Program</a> or the <a href="/adult_competitive.php">Masters Competitive
Program</a> at the start of any season with the approval of the coach.
</p>

