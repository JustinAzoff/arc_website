<h2>The Head of the Hudson Regatta</h2>
<p>
Each year in the fall, the Albany Rowing Center hosts the Head of the Hudson
Regatta.  Typical of other "head" races that are held throughout the country in
the fall, the Head of the Hudson is a three-mile race "against the clock."  The
regatta consists of thirty or more different race events, in which adults,
juniors and recreational rowers, both novice and experienced, may compete.
Competing rowers begin at a start point three miles north of the Albany Rowing
Center boathouse on the Hudson River in Albany and, one after the other, race
south to finish just past the boathouse.  The best finishing time in each race
event determines the winner.  Gold and silver medals are awarded to the first
and second-place finishers in each race event, and special trophies are also
awarded to winning teams of certain sponsored races.  An All-Points Trophy is
awarded at the end of the regatta to the team with the most points overall,
based on placement in each race event.  
</p>


<h2>Albany Rowing Center</h2>
<p>
Founded in 1985, the Albany Rowing Center, located on the Hudson River in
downtown Albany, is a 501(c)(3) not-for-profit organization committed to
providing recreational and competitive rowing opportunities for both youth and
adults.  We use membership dues and equipment fees to meet operational expenses
and to purchase equipment.  We rely on fund raising activities and sponsorships
to cover the costs of the events that we host.   
</p>

<h2> Events </h2>
<p>
We conduct several rowing events each year.  We celebrate National Learn to Row
Day in early June, and conduct a Community Rowing Challenge on the same day.
We host a number of scrimmages against other crews during the course of our
summer season.  We host the Head of the Hudson Regatta each fall at the end of
September.  The Head of the Hudson is our premier event, bringing together
hundreds of competitors from high schools, colleges and clubs from the Capital
District, other areas of New York and surrounding states.  This year's Head of
the Hudson Regatta is scheduled for Saturday, September 27.  The regatta will
take place from approximately 7:00 am to 2:00 pm, and we plan to run more than
thirty different race events during the course of the day.
</p>

