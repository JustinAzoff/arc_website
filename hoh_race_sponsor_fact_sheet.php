competing rowers, their coaches, their families and friends, the race
officials, the various organizations that provide support assistance for the
regatta and the other individuals and businesses that have also chosen to
sponsor races and support the regatta. Together, these people come from the
Capital District, other regions of New York State and several other states as
well.
</p>

