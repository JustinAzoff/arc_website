The goal of the adult coached/intermediate program is to provide directed,
structured practices for both continuing beginners from the learn to row
program and experienced rowers who wish to improve rowing technique, fitness
and/or readiness for competition. Participants in this program may transfer to
the <a href="adult_competitive.php">masters rowing</a> program or the
recreational rowing program at the start of any season with the approval of the
coach.
</p>

<p>
Both morning and evening sessions are offered three days a week.  Participants
in the morning session will row at the same time as the
<a href="adult_competitive.php">master rowers</a> and be coached by either the
masters coach or an assistant. Participants in the evening session will row on
the same schedule as the <a href="adult_recreational.php">recreational
rowers</a> and will be coached by an assistant coach. Evening intermdiate
rowers seeking to achieve a competitive workout may join the adult competitive
practice on Saturdays.
