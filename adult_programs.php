<h2>Visiting Rowers</h2>
<p>
Traveling through the Capital Region? Need your rowing fix? Stop by ARC!
</p>
<p>
ARC offers opportunity for guest rowers to row while in the Albany area.
Experienced rowers who live outside of the Capital District (Albany,
Rensselaer, Saratoga, and Schenectady counties) and who are visiting the Albany
area may row in any of our adult programs while they are here. The guest rower
fees are $15 per day or $35 per week for part-time rowing and $45 per week for
full time rowing. Guest rowers must sign an ARC waiver and release form. As a
rule, no guest may row more than 2 consecutive weeks in one season without
joining ARC as a member.
</p>

