<h2> Morning Rowing Group </h2>
<p>
The emphasis of the Morning Rowing Group is on competitive training with the
goal of participating at local and regional regattas. Participants in this
program should be committed to train hard under the direction of Coach Mike
MacMinn and be willing to travel to selected regattas. Practice schedule will
be 5 mornings per week, as determined by the coach.
</p>
<h3> Practice Time: </h3>
<p>
Weekdays from 5:30 to 7:00 AM; Saturdays from 7:00 to 8:30 AM.
</p>
<h3>Coach</h3>
<p> Mike MacMinn </p>

<h2> Evening Rowing Group </h2>
<p>
The practice schedule for the Evening Rowing Group is geared toward rowers who
cannot or do not want to commit to competitive racing at regattas. Rowers are
coached to gain fitness, power and speed while continuing to improve technique.
Intramural races will be arranged during the year to provide competitive rowing
experience to those interested and for new members being introduced to rowing
and racing.
</p>

<h3> Practice Time </h3>
<p>
Weekday evenings from 6:30 to 8:00 PM; Saturdays from 7:00 to 8:30 AM for those
choosing a full time schedule (5 days per week). Part time rowers will row 3
days per week as determined by the coach.
</p>

<h3> Coach</h3>
<p>Michael Warner</p>

<h2> General Information</h2>
<p>
Novice rowers are welcome to join the Evening Rowing Group at the discretion of
the coach. Any evening rower wishing to join the Morning Rowing Group may do so
with the consent of the coach. If you are getting back into rowing after being
injured or not rowing for years, we recommend starting with the evening
program. If you are new to the Albany area and looking for a place to row but
are not sure of your level of commitment, we offer a free row as a test drive
before you sign up.
</p>

<h2> Season Dates and Dues </h2>

<h3>Regattas &amp; Virtual Racing Account</h3>

<p>
The competitive program typically attends several local and regional regattas
each year. A schedule of upcoming regattas will be posted when available.
Virtual Racing Accounts (VRAs) are used to manage regatta fees. <strong>Each
rower's VRA should be brought up to $175 before the start of each
season.</strong> As you participate in races, Albany Rowing Center will deduct
the "actual cost" of attending the regatta from your VRA. "Actual costs" are
the cumulative cost of:
</p>
<ul>
    <li>Entry Fees</li>
    <li>Gas</li>
    <li>Tolls</li>
    <li>Coach's costs</li>
</ul>

Divided by the number of racers participating in that regatta.

