<p>
<strong>Welcome to Albany Rowing Center!</strong>
<p>
Founded in 1985, the Albany Rowing Center, located on the Hudson River in
downtown Albany, is committed to providing recreational and competitive rowing
opportunities for both youth and adults.  Albany Rowing Center (ARC) is a
not-for-profit organization that fosters interest in and provides opportunity
for rowing in New York's Capital District.  ARC rowers represent all ages (from
middle school students on up) and a wide range of rowing abilities (from novice
to experienced).
</p>
<p>
We conduct several rowing events each year. We celebrate National Learn to Row
Day in early June, and conduct a Community Rowing Challenge on the same day. We
host a number of scrimmages against other crews during the course of our summer
season. We host the Head of the Hudson Regatta each fall at the end of
September. The Head of the Hudson is our premier event, bringing together
hundreds of competitors from high schools, colleges and clubs from the Capital
District, other areas of New York and surrounding states.  </p>

<h1>News</h1>

<h2>Changes to this year's Head of the Hudson Regatta</h2>
<p>
This year, the twenty fourth in the series, we've made some changes to the
event. We have shortened the usual 3 mile race to be just under 2 miles (3.2K)
and it is much more spectator friendly, with viewing possible at every point
along the course. Competing rowers begin at a start point just north of the I
90 bridge, upstream of the Albany Rowing Center boathouse on the Hudson River
and, one after the other, race south to finish at the amphitheater at the
Corning Preserve.  <a href="hoh.php">Read More about the changes to the Head of the Hudson</a>
</p>

