<h2>Adult Learn to Row</h2>

<p>
This program is for adults interested in learning the basic techniques and
styles of sculling and sweep rowing. The program is a low impact sport
available to adults of all athletic levels. Each monthly Learn to Row session
consists of four weeks of rowing three nights per week. The twelve day program
includes both instruction on land and learning basic rowing technique on the
water. On water activities are dependent on weather and river conditions. After
completing the basic four week program, those interested in continuing to row
can take a second Learn to Row session or advance to one of our regular adult
rowing programs, at the discretion of the coach.
</p>

<h2>Prerequisite</h2>

<p>
You must be able to swim in order to participate in any ARC program.
Participants must complete a swimmer affirmation form certifying that they are
able to tread water for 10 minutes and swim a distance of 100 yards (4 pool
laps) without stopping. The coach's launch is equipped with life jackets for
each participant in the event that a rower goes overboard. All participants
must view and understand the USRowing Safety video which is shown on the first
night of practice.
</p>

<h2>Session Dates and Practice Times</h2>

<p>
Evenings: Monday, Wednesday and Thursday at 6:30 - 8:00 p.m. Make up dates may
be scheduled for holidays and weather cancellations at the discretion of the
coach.  Participation Fee is $200 for the 12 day session. Morning sessions may
be offered if there is sufficient interest (12 participants).
</p>
