<h2>The Head of the Hudson Regatta</h2>
<p>
Each year in the fall, the Albany Rowing Center hosts the Head of the Hudson
Regatta. Typical of other "head" races that are held throughout the country in
the fall, it is a race "against the clock." The Head of the Hudson is our
premier event, bringing together hundreds of competitors from high schools,
colleges, and clubs from the Capital District, other areas of New York, and
surrounding states. The regatta consists of twenty different race events, in
which adults, juniors and recreational rowers, both novice and experienced, may
compete. The best finishing time in each race event determines the winner. Gold
and silver medals are awarded to the first and second-place finishers in each
race event, and special trophies are also awarded to winning teams of certain
sponsored races. An All-Points Trophy is awarded at the end of the regatta to
the team with the most points overall, based on placement in each race event.
</p>
<p>
The regatta is both ARC's coveted home regatta as well as the focal point for
our fall fundraising drive. Founded in 1985, the Albany Rowing Center, located
on the Hudson River in downtown Albany, is committed to providing recreational
and competitive rowing opportunities for both youth and adults. We use
membership dues and equipment fees to meet operational expenses and coaching
salaries. We rely on fundraising activities and sponsorships to cover the costs
of the events that we host as well as to contribute toward special equipment
needs such as new oars or boats. The Albany Rowing Center is a 501(c)(3)
not-for-profit organization. Contributions are tax-deductible to the extent
allowed by law. 
</p>

<h2>Changes to this year's Head of the Hudson Regatta</h2>
<p>
This year, the twenty fourth in the series, we've made some changes to the
event. We have shortened the usual 3 mile race to be just under 2 miles (3.2K)
and it is much more spectator friendly, with viewing possible at every point
along the course. Competing rowers begin at a start point just north of the I
90 bridge, upstream of the Albany Rowing Center boathouse on the Hudson River
and, one after the other, race south to finish at the amphitheater at the
Corning Preserve.
<p>

