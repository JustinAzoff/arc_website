<h1> Directions </h1>
<p>
Albany Rowing Center rows out of the City of Albany Boat Shed at the Corning
Preserve Public Boat Launch. The boathouse is located under the elevated
roadway of Interstate 787, north of the intersection of Colonie and Water
Streets. There is ample parking in the OGS lot at this location [except during
the City of Albany's
<a href="http://www.albanyny.org/Government/Departments/GeneralServices/SpecialEvents/AliveAtFive.aspx">Alive at Five</a>
programs on Thursdays during June through August].
</p>

<h2>From the North and Interstate 90 </h2>
<p>
Take Interstate 787 South.
From 787 South, take exit Exit 4A Colonie Street. This will put you on to Water Street.
You will see the boatyard and parking lot on your left under the 787 roadway.
Turn left into the parking lot.
</p>

<h2>From the South </h2>
<p>
Take Interstate 787 north to Exit 4 "Downtown Albany".
Stay right and follow the service road along the river to Colonie Street.
Follow the signs for Corning Preserve and Colonie Street.
As you pass under the railroad bridge, the road curves left and the parking lot is on your right.
</p>

<h2>From Dunn Memorial Bridge</h2>
<p>
Take I-787 North exit marked "Troy" on the bridge.
Get off almost immediately at Clinton Ave. exit.
Stay right and follow the sign for Colonie St. Proceed straight through the light.
Take the first left onto Erie St. Enter the parking lot straight ahead.
</p>
