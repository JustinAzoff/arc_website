<h1> Programs and Camps: Adult Competitive </h1>

<p>
This program is designed for experienced rowers and novice or intermediate
rowers who want to train hard and row competitively. Currently, the primary
focus is on sculling in quads and doubles, however, participants will continue
training in sweep rowing at the discretion of the coach. This program attracts
former collegiate rowers, graduates from the ARC intermediate programs and
collegiate rowers who are living in the Capital District for the summer. These
athletes compete in select regional regattas.
</p>

