<p>
Virtual Racing Account (VRA): $175 at the start of each season
</p>
<p>
As you participate in races, Albany Rowing Center will deduct the "actual cost"
of attending the regatta from your VRA.
</p>

<p>"Actual costs" are the cumulative cost of:</p>
<ul>
    <li> Entry Fees </li>
    <li> Gas </li>
    <li> Tolls </li>
    <li> Coach's costs </li>
</ul>

<p> Divided by: </p>
<ul>
    <li> Number of racers participating in that regatta </li>
</ul>

At the end of each racing season, racers may choose from the following options:
<ul>

    <li> Apply their balance to the next racing season's VRA account </li>
    <li> Donate the balance to ARC </li>
    <li> Cash-out their balance </li>
</ul>


