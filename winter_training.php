</p>

<h3> Payment </h3>
<p>
Forms and checks will be collected at the first day of practice. Bring the
application with you. Be sure to include your choice of session on the
application form.  Credit card payments will also be accepted. 
</p>

<h3> Indoor Rowing Competition</h3>
<p>
As information on events becomes available, rowers may be offered the
opportunity to participate in indoor erg competitions. The Crash B's are at the
end of February and the Hudson River Indoor Sprints are held at Marist in
mid-March. Registration fees for these events will be collected separately. 
</p>

