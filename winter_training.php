<h2>Program Description</h2>
<p>
The 2009 - 2010 winter training program will prepare varsity rowers for spring
competition and improve athleticism and rowing potential for novices,
non-competitive adults and those returning to rowing after a break in training.
Although winter training attendance can be more flexible than participation in
the regular rowing season, the more you put into your participation the more
you will improve.  Winter training will take place from December 14<super>th</super>
through March 13<super>th</super> at our rented space on the first floor of 883
Broadway in Albany at the corner of Broadway and North Ferry Street.
</p>

<h2> Winter Training Schedule</h2>

<h3>Juniors</h3>
<p>
Two sessions are offered between 3:30 and 6:30 pm weekdays. Varsity rowers will
train 6 days per week; novices and non-varsity will train 3 or 4 days per week.
Logistics for session assignments and Saturday rowing will be worked out by
Coach Mike MacMinn. 
</p>

<h3> Adult coached</h3>
<p>
Weekday evenings (MWTh) at 6:30 and Saturday mornings time TBD and arranged by
Coach Mike Warner. 
</p>

<h3> Collegiate/adult uncoached </h3>
<p>
Weekday mornings between 7:00 and 10:00 am. Mike MacMinn will be the on-site
ARC person in charge. This is for self-motivated individuals who can train
without a coach, on their own or with each other. 
</p>

<h3> Fees </h3>
<ul>
    <li> Junior Varsity  - $300 </li>
    <li> Junior Novice/non-varsity - $265 </li>
    <li> Adult coached  - $265 </li>
    <li> Collegiate/adult uncoached - $60 per month (equipment use fee) </li>
</ul>

<h3> Forms </h3>
<p>
<a href="/docs/WINTER TRAINING REGISTRATION FORM 09_10.pdf">Registration form</a>,
