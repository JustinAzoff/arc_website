<h2>Program Description</h2>
<p>
The 2010 - 2011 winter training program will prepare Juniors and Masters for
spring competition and improve athleticism and rowing potential for
non-competitive adults and those returning to rowing after a break in
training. This is the time when athletes build strength and power for the
spring regatta season. Winter training will take place from December
1<super>st</super> 2010 through March 12<super>th</super> 2011 at our rented
space on the first floor of 883 Broadway in Albany at the corner of Broadway
and North Ferry Street.
</p>

<h2> Winter Training Schedule and Fees</h2>

<h3>Juniors</h3>
<p>
Juniors will train weekday afternoons [at 3:30 to 5:00 or 5:00 to 6:30 pm] and
Saturday mornings [8:00 to 9:30 or 9:30 - 11:00 am]. Session assignments will
be worked out by Coach Mike MacMinn after all rowers are registered.
</p>
<ul>
    <li>High school/varsity will train 6 days per week. <strong>Fee = $300</strong></li>
    <li>Middle school/modified will train 3 days per week. <strong>Fee = $250</strong></li>
</ul>

<h3> Adults </h3>
<p>
Adults can choose either morning (weekdays at 5:45 until 7:00 am) or evening
(weeknights at 6:30 until 8:00 pm) training. Saturday morning training will be
available at 7:00 am as equipment allows. Adults may combine AM and PM sessions
with permission of the coach. Michael Warner and Mike MacMinn will share
coaching. Some days will be uncoached. <strong>Fee = $265</strong>
</p>

<h3>Drop-in</h3>
<p>Fee - $5.00 per day</p>

<h3> Forms </h3>
<p>
Required registration form, waiver and parental approval (for juniors). Medical
form only required if you are a new member; your information has changed; or
there is a particular allergy or medical condition of which the coach should be
aware. If so, bring medical information to the first day of practice.
</p>

<h3> Payment </h3>
<p>
We will hold on-site registration on the first 3 days of practice. You must
register during this time. Bring your check or money order for the appropriate
amount made out to Albany Rowing Center with "Winter Training" written on the
notes line. No cash, please.  Any rower who does not pay will not be allowed to
train. Late registration after the 3<super>rd</super> practice will incur a $10
late fee, except for new members. Do not give money to coaches. There will be
no refunds of winter training dues.
</p>

<h3> Indoor Rowing Competition</h3>
<p>
As information on events becomes available, rowers may be offered the
opportunity to participate in indoor erg competitions. The Crash-Bs are on
February 20, 2011 and the Hudson River Indoor Sprints are held at Marist in
mid-March (may be on March 12, 2011).  Registration fees for these events will
be collected separately.
</p>

