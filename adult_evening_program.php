<h1> <?= $title ?> </h1>

<p>
The goal of the adult evening program is to provide directed, structured
practices for experienced rowers who wish to improve rowing technique,
fitness and/or readiness for competition. Evening sessions are offered
three days a week.
</p>

<p>
Rowers wishing to get a more competitive workout may add the evening
competitive option, which will meet an additional one to two days per
week (likely Tuesday evenings and Saturday mornings) and will be directed
towards racing at the Head of the Hudson Regatta.
</p>

<p>
Participants may transfer into the evening program from the
<a href="adult_novice.php">Adult NoviceProgram</a>
at the start of any season with the approval of the coach.  Participants
in the adult evening program may transfer to the
<a href="adult_competitive.php">Masters Competitive Program</a>
at the start of any season with the approval of the coach.  </p>

