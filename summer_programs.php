<h2>Junior Rowing Summer Camp</h2>

<p>
Junior Rowing Summer Camp Program offers instruction in rowing to boys and
girls who have finished 7th grade. Coach Evan Bollin teaches young athletes
about the sport of rowing through participation in a fun and educational
setting. Students will learn water and boat safety, stroke mechanics on the
ergometer (rowing machine) and the basic techniques of sweep rowing and
sculling. While the majority of time is spent on the water, there is also
fitness training on land and education about boats and equipment.  Campers
learn how to work as a team. Those who would like to continue rowing may join
the fall competitive rowing program.
</p>

<h2>Swim Test</h2>

<p>
The swim test can be taken at <em>any pool</em>where a lifeguard is present. We
do not administer swim tests at the boathouse. Please print out the Swim Test
Certification and have a lifeguard sign it. You can either mail the form to
Albany Rowing Center OR bring it to your first day of class. You may not be
allowed to get in the boat without the swim test.
</p>

