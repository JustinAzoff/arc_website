<h1> <?= $title ?> </h1>

<p>
The Albany Rowing Center Junior Summer Camp Program offers instruction in
rowing  to interested youth who have completed seventh grade. The Learn to Row
Program permits young athletes to learn about the sport of rowing through
participation. Students will learn water and boat safety, stroke mechanics on
the ergometer (rowing machine) and the basic techniques of sweep rowing and
sculling. While the majority of time is spent on the water, there is also
fitness training on land and education about boats and equipment. Students who
wish to continue rowing may join the fall competitive rowing program.
</p>

<h2> Swim Test </h2>

<p>
The swim test can be taken at <em>any pool</em> where a lifeguard is present.
We do not administer swim tests at the boathouse. Please print out the
<a href="/docs/Junior Swim Test Certification Form.pdf">Swim Test Certification</a>
and have a lifeguard sign it.  You can either mail the form to Albany Rowing
Center <em>or</em> bring it to your first day of class. You may not be boated
without the swim test.
</p>

